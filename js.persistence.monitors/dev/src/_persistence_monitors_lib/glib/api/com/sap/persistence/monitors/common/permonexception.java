package com.sap.persistence.monitors.common;

public class PerMonException extends Exception {

	
	private static final long serialVersionUID = 1809423154988488503L;
	
	public PerMonException() {
	    super();
	}

	public PerMonException(String message) {
	    super(message);
	}

	public PerMonException(Throwable cause) {
	    super(cause);
	}

	public PerMonException(String message, Throwable cause) {
	    super(message, cause);
	}
	
	
	
	    
}
