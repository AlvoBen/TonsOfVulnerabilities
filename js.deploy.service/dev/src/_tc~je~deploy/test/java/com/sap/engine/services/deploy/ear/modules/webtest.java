package com.sap.engine.services.deploy.ear.modules;

import java.io.File;
import java.util.HashSet;

import junit.framework.TestCase;

/**
 * @author Luchesar Cekov
 */
public class WebTest extends TestCase {  ;

  public void testEquals() throws Exception {
    HashSet old = new HashSet();
    for (int i = 0; i < 100; i++) {
      Web web = new Web(new File(random()), random(), random());
      assertFalse(old.contains(web));
    }
    
    Web web1 = new Web(new File(""), "webUri", "contextRoot");
    Web web2 = new Web(new File("werwer"), "webUri", "contextRoot123123");
    
    assertEquals(web1, web2);
    
    web1 = new Web(new File(""), "webUri", "contextRoot");
    web1.setAutogenerated(true);
    web2 = new Web(new File("werwer"), "webUri", "contextRoot123123");
    web2.setAutogenerated(false);
    
    assertEquals(web1, web2);
  }
  
  private String random() {
    return System.currentTimeMillis() + "" + Math.random();
  }
}
