import org.w3c.dom.Element;

import com.sap.engine.lib.log.LogWriter;
import com.sap.engine.lib.xsl.xpath.XPathContext;
import com.sap.engine.lib.xsl.xpath.XPathException;
import com.sap.engine.lib.xsl.xpath.xobjects.XObject;

public final class XSLTopVariable extends XSLVariable {

  public XSLTopVariable(XSLStylesheet owner, Element el) throws XSLException {
    super(owner, null, el);
  }

  public XObject evaluate(XPathContext xcont) throws XPathException, XSLException {
    return super.evaluate(owner.getDTM().getInitialContext());
  }

  public void process(XPathContext xcont, int node) throws XSLException, XPathException {
    super.process(xcont, node);
//    if (getNext() != null) {
//      getNext().process(xcont, node);
//    }
  }

  public void print(String ind) {
    LogWriter.getSystemLogWriter().println(ind + "#XSLTopVariable: select = " + select + " , name = " + name); //$JL-SYS_OUT_ERR$
    if (getFirst() != null) {
      getFirst().print(ind + "  ");
    }
    if (getNext() != null) {
      getNext().print(ind);
    }
  }

}

