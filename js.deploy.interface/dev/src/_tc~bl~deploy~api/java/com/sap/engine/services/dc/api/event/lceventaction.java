public class LCEventAction {
	public static final LCEventAction COMPONENT_STARTED = new LCEventAction(
			new Integer(0), "Component started", "Component has been started.");

	public static final LCEventAction COMPONENT_STOPPED = new LCEventAction(
			new Integer(1), "Component stopped", "Component has been stopped.");

	private final Integer id;
	private final String name;
	private final String description;

	private LCEventAction(Integer id, String name, String description) {
		this.id = id;
		this.name = name;
		this.description = description;
	}

	public Integer getId() {
		return this.id;
	}

	public String getName() {
		return this.name;
	}

	public String getDescription() {
		return this.description;
	}

	public String toString() {
		return this.name;
	}

	public boolean equals(Object obj) {
		if (obj == null) {
			return false;
		}

		if (this == obj) {
			return true;
		}

		if (!(obj instanceof LCEventAction)) {
			return false;
		}

		final LCEventAction other = (LCEventAction) obj;

		if (!this.getId().equals(other.getId())) {
			return false;
		}

		return true;
	}

	public int hashCode() {
		return this.id.hashCode();
	}
}
